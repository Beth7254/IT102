#include<stdio.h>

void main()
{
    int number=12;
    int numberTwo=2;
    printf("the sum of %d and %d is %d\n", number, numberTwo, number + numberTwo);
    printf("the difference of %d and %d is %d\n", number, numberTwo, number - numberTwo);
    printf("the product of %d and %d is %d \n", number, numberTwo, number * numberTwo);
    printf("the number of %d and %d is %d \n", number, numberTwo, number / numberTwo);
}